package org.hdl.anima.remoting;


/**
 * 
 * @author qiuhd
 * @since  2014年11月5日
 * @version V1.0.0
 */
public class CodecException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8414906403746957555L;

	public CodecException() {
		super();
	}

	public CodecException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CodecException(String arg0) {
		super(arg0);
	}

	public CodecException(Throwable arg0) {
		super(arg0);
	}
}
